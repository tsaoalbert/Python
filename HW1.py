"""
 (1a)  Compute the series of daily returns from the data. The return is nothing but   (St−S t−1)/(St−1)
"""
def dailyReturn ( x,y ) :
    return (y-x)/x    # x = S_t and y=S_t-1

def dailyReturnArray ( arr ) :
    prev = arr[0]
    ans = array([]) 
    for x in arr[1:]:
        y = dailyReturn ( prev, x);
        ans = append(ans, y);
        prev = x;
    return ans

def twoDayReturnArray ( arr ):
    prev = arr[0]
    ans = array([]) 
    n = len ( arr ) 
    for x in arr[1: n : 2]:
        y = dailyReturn ( prev, x);
        ans = append(ans, y);
        prev = x;
    return ans

def estimated_autocorrelation(x):
    n = len(x)
    variance = x.var()
    x = x-x.mean()
    r = N.correlate(x, x, mode = 'full')[-n:]
    result = r/(variance*(N.arange(n, 0, -1)))

